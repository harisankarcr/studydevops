public class AddTwoNumbers {
    public static void main(String[] args) {
        // Define two numbers
        int num1 = 10;
        int num2 = 20;

        // Add the numbers
        int sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
